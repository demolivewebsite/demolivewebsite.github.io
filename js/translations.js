// Language Manager Class
// ========================================
class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('preferred_language') || 'en';
        this.init();
    }

    init() {
        // Set initial language
        this.setLanguage(this.currentLang);
        
        // Set up language toggle buttons
        this.setupToggleButtons();
    }

    setupToggleButtons() {
        const langBtns = document.querySelectorAll('.lang-btn');
        
        langBtns.forEach(btn => {
            // Set active state based on current language
            if (btn.dataset.lang === this.currentLang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }

            btn.addEventListener('click', () => {
                const lang = btn.dataset.lang;
                this.setLanguage(lang);
                
                // Update active states
                langBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
            });
        });
    }

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('preferred_language', lang);
        
        // Update HTML lang attribute
        document.documentElement.lang = lang;
        
        // Update body class for font styling
        document.body.classList.remove('lang-en', 'lang-mr');
        document.body.classList.add(`lang-${lang}`);
        
        // Translate all elements
        this.translatePage();
        
        // Update placeholders
        this.translatePlaceholders();
        
        // Update select options
        this.translateSelectOptions();
    }

    translatePage() {
        const elements = document.querySelectorAll('[data-translate]');
        
        elements.forEach(el => {
            const key = el.dataset.translate;
            const translation = translations[this.currentLang][key];
            
            if (translation) {
                // Check if translation contains HTML (like span tags)
                if (translation.includes('<')) {
                    el.innerHTML = translation;
                } else {
                    el.textContent = translation;
                }
            }
        });
    }

    translatePlaceholders() {
        const inputs = document.querySelectorAll('[data-placeholder]');
        
        inputs.forEach(input => {
            const key = input.dataset.placeholder;
            const translation = translations[this.currentLang][key];
            
            if (translation) {
                input.placeholder = translation;
            }
        });
    }

    translateSelectOptions() {
        const selects = document.querySelectorAll('select');
        
        selects.forEach(select => {
            const options = select.querySelectorAll('option[data-translate]');
            
            options.forEach(option => {
                const key = option.dataset.translate;
                const translation = translations[this.currentLang][key];
                
                if (translation) {
                    option.textContent = translation;
                }
            });
        });
    }

    // Method to get translation for dynamic content
    getTranslation(key) {
        return translations[this.currentLang][key] || key;
    }

    // Get current language
    getCurrentLanguage() {
        return this.currentLang;
    }
}

